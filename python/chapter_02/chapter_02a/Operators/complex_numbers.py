# Complex numbers
complex_num1 = 3 + 4j
complex_num2 = 2 - 1j

# Addition
result_addition = complex_num1 + complex_num2
print("Addition of Complex Numbers:", result_addition)

# Subtraction
result_subtraction = complex_num1 - complex_num2
print("Subtraction of Complex Numbers:", result_subtraction)

# Multiplication
result_multiplication = complex_num1 * complex_num2
print("Multiplication of Complex Numbers:", result_multiplication)
